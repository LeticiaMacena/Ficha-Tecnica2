        private void carregarComboFuncionario() {
         //assim como é feito numa JTable, temos o DefaultComboBoxModel que é o model do JComboBox
        DefaultComboBoxModel comboModel = (DefaultComboBoxModel) comboFuncionarioResponsavel.getModel();
        //removendo todos os elementos do combo
        comboModel.removeAllElements();
        //cria a lista: java.util.List
        List<Funcionario> funcionarios = new ArrayList<Funcionario>();
        FuncionarioController a = new FuncionarioController();
        funcionarios = a.listarFuncionario();
        
     
        for (int linha = 0; linha <funcionarios.size(); linha++) {
            //pegando a categoria da lista
            Funcionario funcionario = funcionarios.get(linha);
            //adicionando a categoria no combo
            comboModel.addElement(funcionario);
        }
    }
