    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        Colecao m = new Colecao();

        if ((campoTema.getText().length() <= 0)

                || ((campoEstacao.getText().length() <= 0)

                    || (campoDataAprovacao.getText().length() <= 0))){
        
        } else {

            m.setTema(campoTema.getText());
            m.setEstacao(campoEstacao.getText());
            m.setFuncionarioReponsavel((Funcionario)comboFuncionarioResponsavel.getSelectedItem());
            try{

                m.setDataAprovacaoModelo(new SimpleDateFormat("dd/mm/yyyy").parse(campoDataAprovacao.getText()));

            } catch (ParseException ex){
                System.out.println("Erro na conversão de datas" + ex.getMessage());
            }
             if (checkFeminino.isSelected()){
               m.setGenero(true);
       
               
           }else{
               m.setGenero(false);
           }
      
             if (checkMasculino.isSelected()){
               m.setGenero(true);
       
               
           }else{
               m.setGenero(false);
           }
             
            ColecaoController mc = new ColecaoController();
            if(mc.salvar(m)){
                model.addColecao(pesquisar(m.getTema()));
            }
            dispose();
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

